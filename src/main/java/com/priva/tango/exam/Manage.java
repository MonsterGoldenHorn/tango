    /**
     * >> 目标
     *  在项目管理软件中进行风险管理，从而降低风险，保障项目的成功完成
     *
     *>>方法
     * 风险管理是一个系统性的过程，需要经过以下几个步骤
     * 1、风险识别：识别和列出可能出现的风险。
     * 2、风险分析：分析每个风险的概率、影响力和优先级。
     * 3、风险评估：确定每个风险的严重程度和应对方案。
     * 4、风险应对：制定和实施风险应对方案。
     * 5、风险监控：监控风险应对方案的执行效果，及时进行调整和优化。
     *
     * >> Boehm体系
     *  Boehm思想的核心是10大风险因素（包括人员短缺、不合理的进度安排和预算、不断的需求变动等），风险评估（识别、分析、设置优先级）和风险控制（制定管理计划、解决和监督风险）两大部分组成
     *  估计+控制
     * >> Charette
     *  美国Charette的风险分析和管理体系，把风险分成分析（辨识、估计、评价）和管理（计划、控制、监督）两大阶段。偏重理论
     *  分析+管理
     */
