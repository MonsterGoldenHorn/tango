    /*配置管理 SCM*/
    /**
     * 配置项  基线配置项（可交付成果），非基线配置（计划报告）
     * 配置库  开发库（变动中），受控库（稳定库，是管理~~~基线~~~），产品库（最终产品）
     * >>定义
     * 软件配置管理(SCM)是指通过执行版本控制、变更控制的规程，以及使用合适的配置管理软件，来保证所有配置项的完整性和可跟踪性。配置管理是对工作成果的一种有效保护
     * >> 目标
     * 软件配置管理是在贯穿整个软件生命周期中建立和维护项目产品的完整性。它的基本目标包括：
     * 目标 1: 软件配置管理的各项工作是有计划进行的。
     * 目标 2: 被选择的项目产品得到识别，控制并且可以被相关人员获取。
     * 目标 3: 已识别出的项目产品的更改得到控制。
     * 目标 4: 使相关组别和个人及时了解软件基准的状态和内容。
     * >>活动
     *  配置项识别、配置项的标识和控制、工作空间管理、【版本控制、变更控制】、状态报告、配置审计
     *
     *  核心内容 版本控制、变更控制
     *  >>软件生命周期
     *      1.可行性分析与项目开发计划
     *      2.需求分析    产出数据量图
     *      3.概要设计    产出模块结构图,层次图，HIPO图(层次图+输入输出关系图)
     *      4.详细设计    产出程序流程图，伪代码，盒图（N-S图）
     *      5.编码，6.测试，维护
     */
